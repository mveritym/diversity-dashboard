$(function() {
    show_file_uploader();
    hide_chart();
})

function show_file_uploader () {
    $("#include-dropzone").show();
}

function hide_file_uploader () {
    $("#include-dropzone").hide();
}

function show_chart () {
    $("#include-chart").show();
}

function hide_chart () {
    $("#include-chart").hide();
}
